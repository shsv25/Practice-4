using System;

namespace Poject4.ex4
{
    internal class Program
    {
            static void Main()
            {
                Console.Write("–í–≤–µ–¥i—Ç—å –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–µ —á–∏—Å–ª–æ: ");
                int num = int.Parse(Console.ReadLine());

                Console.Write("–†–æ–∑–∫–ª–∞–¥ —á–∏—Å–ª–∞ {0} –Ω–∞ –ø—Ä–æ—Å—Çi –º–Ω–æ–∂–Ω–∏–∫–∏: ", num);
                PrintPrimeFactors(num); // –≤–∏–∫–ª–∏–∫ –º–µ—Ç–æ–¥—É —Ä–æ–∑–∫–ª–∞–¥—É —á–∏—Å–ª–∞ –Ω–∞ –ø—Ä–æ—Å—Ç—ñ –º–Ω–æ–∂–Ω–∏–∫–∏

                Console.WriteLine();
            }

            static void PrintPrimeFactors(int num)
            {
                for (int i = 2; i <= num; i++)
                {
                    int count = 0;
                    while (num % i == 0)
                    {
                        count++;
                        num /= i;
                    }
                    for (int j = 0; j < count; j++)
                    {
                        Console.Write("{0} ", i);
                    }
                }
            }
        }
    }
